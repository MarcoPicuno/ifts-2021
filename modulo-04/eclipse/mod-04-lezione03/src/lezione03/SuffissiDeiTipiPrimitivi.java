package lezione03;

public class SuffissiDeiTipiPrimitivi {

	public static void main(String[] args) {
		// il valore letterale numerico intero è interpretato come int
		int i = 1;
		long l = 1L;
		
		// il valore letterale numerico decimale è interpretato come double
		float f = 1.1F;
		double d = 1.1;
	}

}
